from datetime import datetime
from embedding_repository import EmbeddingRepository

repo = EmbeddingRepository()

started_at = datetime.now().isoformat(timespec="seconds")
print(f"Started at: {started_at}")

sample_text = """
Солнечная система состоит из звезды Солнце и объектов, которые обращаются вокруг неё под действием гравитации. 
К таким объектам относятся восемь планет, их спутники, карликовые планеты, астероиды, кометы и множество более мелких тел.

Меркурий — ближайшая к Солнцу планета. Он делает полный оборот вокруг Солнца примерно за 88 земных суток.
Из-за отсутствия плотной атмосферы температура на его поверхности сильно меняется между днём и ночью.

Венера по размеру похожа на Землю, но её атмосфера почти полностью состоит из углекислого газа.
Из-за сильного парникового эффекта Венера считается самой горячей планетой Солнечной системы.

Земля — единственная известная планета, на которой существует жизнь. 
Большая часть поверхности Земли покрыта водой, а атмосфера содержит кислород, необходимый большинству живых организмов.

Марс известен как красная планета из-за большого количества оксида железа в грунте.
Учёные предполагают, что в далёком прошлом на Марсе могла существовать жидкая вода.

Юпитер — крупнейшая планета Солнечной системы. 
Он состоит в основном из водорода и гелия и обладает мощным магнитным полем.
Одной из самых известных особенностей Юпитера является Большое красное пятно — гигантский атмосферный вихрь.

Сатурн знаменит своей системой колец, состоящих из льда, пыли и каменных частиц.
Хотя кольца есть и у других планет-гигантов, у Сатурна они наиболее заметны.

Уран и Нептун называют ледяными гигантами.
Нептун известен очень сильными ветрами в атмосфере, а Уран вращается почти «лёжа на боку» по сравнению с другими планетами.
"""

print("=== 1. Разбиение текста на чанки ===\n")
chunks = repo.get_chunks(sample_text, chunk_size=220, overlap=40)

print(f"Получено {len(chunks)} фрагментов:\n")
for i, chunk in enumerate(chunks):
    preview = chunk[:120] + "..." if len(chunk) > 120 else chunk
    print(f"[{i}] {preview}")

print("\n=== 2. Генерация эмбеддингов ===\n")
embeddings = repo.get_embeddings(chunks)
print(f"Форма массива эмбеддингов: {embeddings.shape}")
print(f"Тип данных: {embeddings.dtype}")

print("\n=== 3. Сравнение двух текстов ===\n")
text1 = "Юпитер является самой большой планетой"
text2 = "Крупнейшая планета Солнечной системы — Юпитер"
text3 = "Яблоки и груши растут в саду"

sim_12 = repo.compare_texts(text1, text2)
sim_13 = repo.compare_texts(text1, text3)

print(f"text1: {text1}")
print(f"text2: {text2}")
print(f"text3: {text3}\n")

print(f"Сходство text1 и text2: {sim_12:.4f}")
print(f"Сходство text1 и text3: {sim_13:.4f}")

print("\n=== 4. Поиск самых похожих чанков ===\n")
queries = [
    "Какая планета самая горячая?",
    "Где есть кольца?",
    "Какая планета самая большая?",
    "На какой планете есть жизнь?",
    "Что известно про Марс?"
]

for query in queries:
    print(f"\nЗапрос: {query}")
    scores = repo.batch_compare(query, chunks)

    ranked = sorted(enumerate(scores), key=lambda x: x[1], reverse=True)

    for idx, score in ranked[:3]:
        preview = chunks[idx][:100] + "..." if len(chunks[idx]) > 100 else chunks[idx]
        print(f"  [{idx}] sim={score:.4f}: {preview}")

finished_at = datetime.now().isoformat(timespec="seconds")
print(f"\nFinished at: {finished_at}")
